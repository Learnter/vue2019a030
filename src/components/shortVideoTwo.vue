<script>
export default {
  name: "shortVideoTwo",
  data(){
    let u = navigator.userAgent;
    return {
      current: 0, //当前播放视频索引
      video_show_id:'',//当前播放视频Id
      isVideoShow: true, //是否显示播放按钮
      playOrPause: true,
      iconPlayShow: false,
      isAndroid: u.indexOf("Android") > -1 || u.indexOf("Adr") > -1, // android终端
      isiOS: !!u.match(/\(i[^;]+;( U;)? CPU.+Mac OS X/), // ios终端
      search_text: "",
      loading: false,
      finished: false,
      videosList: []
    };
  },
  created() {

  },
  methods: {
    fetchVideoList() {
      //获取短视频列表
      let url = "video/shortVideoList";
      this.$https.get(url).then(res => {
        if (res.data.code === 200 && res.data.data) {
          // console.log(res.data);
          this.videosList = res.data.data;
          this.loading = false;
          this.finished = true;
        }
      });
    },
    playVideo(obj,index) {
      console.log("点击播放按钮........");
      this.current = index;
      this.video_show_id = obj.id;

      let video = document.querySelectorAll("video")[this.current];
      console.log("playvideo：" + this.current);

      this.isVideoShow = false;
      this.iconPlayShow = false;
      video.play();
    },
    pauseVideo() {
      //暂停\播放
      let video = document.querySelectorAll("video")[this.current];
      console.log("pauseVideo" + this.current);
      if (this.playOrPause) {
        video.pause();
        this.iconPlayShow = true;
      } else {
        video.play();
        this.iconPlayShow = false;
      }
      this.playOrPause = !this.playOrPause;
    },
    onPlayerEnded(player) {
      //视频结束
      this.isVideoShow = true;
      this.current += this.current;
    },
    onSearch() {
      console.log("点击搜索按钮" + this.search_text);
    },
    playvideo(){ //点击暂停、播放按钮

    },
    onLoad() {
      this.fetchVideoList();
    }
  }
};
</script>
